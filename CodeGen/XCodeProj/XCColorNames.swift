        }
        var cl: String?
        var ndf1: CGFloat = 0
        var ndf2: CGFloat = 0
        var ndf: CGFloat = 0;
        var df: CGFloat = -1

        for (name, value) in kColorNames {
            if target.redComponent == value.redComponent && target.greenComponent == value.greenComponent
                && target.blueComponent == value.blueComponent {
                return name
            }
            ndf1 = pow(target.redComponent - value.redComponent, 2) + pow(target.greenComponent - value.greenComponent, 2) + pow(target.blueComponent - value.blueComponent, 2)
            ndf2 = pow(target.hueComponent - value.hueComponent, 2) + pow(target.saturationComponent - value.saturationComponent, 2) + pow(target.brightnessComponent - value.brightnessComponent, 2)
            ndf = ndf1 + ndf2 * 2
            if df < 0 || df > ndf {
                df = ndf
                cl = name
            }
        }
        return cl
    }

